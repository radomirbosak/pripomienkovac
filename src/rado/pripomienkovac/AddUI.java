    private void zobrazZaznam(Integer id) {
        Note n = Note.notes.get(id.intValue());
        //@FIXME ak sa nenachadza key
        if (n.content instanceof ContentTitleString) {
            jTextField4.setText(((ContentTitleString)n.content).getTitle());
            jTextArea1.setText(((ContentTitleString)n.content).getString());
        } else if (n.content instanceof ContentSimpleString) {
            jTextArea1.setText(((ContentSimpleString)n.content).getString());
            
        }
        if (n.trigger instanceof TriggerSimpleDate) {
            DataDate datum = ((TriggerSimpleDate)n.trigger).datum;
            jTextField1.setText(String.valueOf(datum.day));
            jTextField2.setText(String.valueOf(datum.month));
            jTextField3.setText(String.valueOf(datum.year));
        }
    }
    
    private void pridajZaznam() throws SietkaException, SQLException {
        // aggregating form data
        int day = Integer.valueOf(jTextField1.getText());
        int mon = Integer.valueOf(jTextField2.getText());
        int year = Integer.valueOf(jTextField3.getText());
        
        String obsah = jTextArea1.getText();
        String title = jTextField4.getText();
        
        DataDate datum;
        try {
            datum = new DataDate(year, mon, day);
        } catch (ExceptionMyDateInvalid ex) {
            throw new RuntimeException("datum not validated");
        }
        Note.add(
                new TriggerSimpleDate(datum),
                new ContentTitleString(title, obsah));
    }
    
    private void zeditujZaznam() throws SietkaException, SQLException {
        // aggregating form data
        int day = Integer.valueOf(jTextField1.getText());
        int mon = Integer.valueOf(jTextField2.getText());
        int year = Integer.valueOf(jTextField3.getText());
        
        String obsah = jTextArea1.getText();
        String title = jTextField4.getText();
        
        DataDate datum;
        try {
            datum = new DataDate(year, mon, day);
        } catch (ExceptionMyDateInvalid ex) {
            throw new RuntimeException("datum not validated");
        }
        
        Note.edit(editId, new TriggerSimpleDate(datum), new ContentTitleString(title, obsah));
    }

    private void validateDate() {
        try {
            int day = Integer.valueOf(jTextField1.getText());
            int mon = Integer.valueOf(jTextField2.getText());
            int year = Integer.valueOf(jTextField3.getText());
            new DataDate(year, mon, day);
            
            jButton1.setEnabled(true);
        } catch (ExceptionMyDateInvalid  ex) {
            jButton1.setEnabled(false);
        } catch (Exception ex) {
            jButton1.setEnabled(false);
        }
    }
    
